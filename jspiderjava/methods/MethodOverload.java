class MethodOverload
{
    public static void main(String args[])
    {
        m1(12.345);
        m1();
        m1();
    }
    static void m1()
    {
        System.out.println("m1()");
    }
    static void m1(int i)
    {
        System.out.println("m1(int i)");
    }
static void m1(double i)
    {
        System.out.println("m1(double i)");
    }
        
    }
